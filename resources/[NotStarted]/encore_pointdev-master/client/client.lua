function startCollectionThread()
    points = {}

    Citizen.CreateThread(function()
        while true do
            Citizen.Wait(0)

            if not isCollecting then
                return
            end

            BeginTextCommandDisplayHelp('pointDevAlert')
            EndTextCommandDisplayHelp(0, false, false, -1)
        
            if IsControlJustReleased(0, 38) then
                local playerCoordinates = GetEntityCoords(PlayerPedId())

                playerCoordinates = (playerCoordinates - vector3(0.0, 0.0, 1.0))
        
                table.insert(points, playerCoordinates)
        
                Blips[#Blips+1] = AddBlipForCoord(mathRound(playerCoordinates.x, 2),mathRound(playerCoordinates.y, 2),mathRound(playerCoordinates.z, 2))
                SetBlipSprite(Blips[#Blips+1],1)
                SetBlipDisplay(Blips[#Blips+1],4)
                SetBlipAsShortRange(Blips[#Blips+1],true)
                SetBlipScale(Blips[#Blips+1],0.7)
                BeginTextCommandSetBlipName("STRING")
                AddTextComponentString("! "..mathRound(playerCoordinates.x, 2),mathRound(playerCoordinates.y, 2),mathRound(playerCoordinates.z, 2))
                EndTextCommandSetBlipName(Blips[#Blips+1])

                TriggerEvent("Notify", "sucess", "Cordenada: <b>"..mathRound(playerCoordinates.x, 2)..", "..mathRound(playerCoordinates.y, 2)..", "..mathRound(playerCoordinates.z, 2).."</b> registrada!")
            end
        end
    end)
end

