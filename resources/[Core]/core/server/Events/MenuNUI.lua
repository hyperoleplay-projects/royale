-- reMapData - Function
-----------------------------------------------------------------------------------------------------------------------------------------
function reMapData(game_data)
	local cntRed = 0
	local _redList = game_data
    
	game_data = {}
    
	for k,v in pairs(_redList) do
		cntRed = cntRed + 1
		game_data[cntRed] = v
	end
    
	return game_data
end
